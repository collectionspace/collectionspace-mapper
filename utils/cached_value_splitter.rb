type = :refname

class CachedTerm
  include Comparable

  def initialize(arr)
    @type = arr[0]
    @subtype = arr[1]
    @value = arr[2]
    if arr[3].is_a?(Hash)
      @refname = arr[3][:refname]
      @csid = arr[3][:csid]
    else
      puts arr.inspect
      exit
    end
  end

  def <=>(other)
    sig <=> other.sig
  end

  def term?
    type["authorit"] || type["vocabular"]
  end

  def non_term?
    !is_term?
  end

  def sig
    "#{type} | #{subtype} | #{value}"
  end

  def to_csid
    %(['#{type}', '#{subtype}', '#{value}', '#{rand_csid}'])
  end

  def to_refname
    %(['#{type}', '#{subtype}', '#{value}', "#{replace_domain}"])
  end

  def replace_domain
    refname.dup.sub(/^urn:cspace:[a-z]+\.collectionspace\.org/,
      "urn:cspace:DOMAIN")
  end

  private

  attr_reader :type, :subtype, :value, :refname, :csid

  def rand_csid
    "#{csid_segment}-#{csid_segment}-#{csid_segment}-#{csid_segment}"
  end

  def csid_segment
    rand(10000).to_s.rjust(4, "0")
  end
end

